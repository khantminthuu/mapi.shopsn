declare(strict_types=1);
namespace Home\Controller;
use Common\Logic\GoodsComboLogic;
use Common\TraitClass\InitControllerTrait;
use Validate\CheckParam;


class GoodsComboController
{
    use InitControllerTrait;

    /**
     * 架构方法
     * @param array
     * $args   传入的参数数组
     */
    public function __construct(array $args = [])
    {   
        $this->args = $args;
        $this->init();

        $this->logic = new GoodsComboLogic($args);
    }
    /**
     * 得到最佳组合
     * @author 刘嘉强
     */
    public function matchGood() :void
    {
        //检测传值                  //检测方法
        $checkObj = new CheckParam($this->logic->getValidateByLogin(), $this->args);

        $status = $checkObj->checkParam();

        $this->objController->promptPjax($status, $checkObj->getErrorMessage());

        $ret = $this->logic->getComboGoods();

        $this->objController->promptPjax($ret, $this->logic->getErrorMessage());

        $this->objController->ajaxReturnData($ret['data'],$ret['status'],$ret['message']);
    }
   
    /**
     * 得到搭配配件表
     * @author 王波
     */
    public function accessories()
    {
        //检测传值                  //检测方法
        $checkObj = new CheckParam($this->logic->getValidateByLogin(), $this->args);

        $status = $checkObj->checkParam();

        $this->objController->promptPjax($status, $checkObj->getErrorMessage());

        $ret = $this->logic->getAccessories();

        $this->objController->promptPjax($ret, $this->logic->getErrorMessage());

        $this->objController->ajaxReturnData($ret['data'],$ret['status'],$ret['message']);
    }
}